        pstmt.setString(1, tfSearch.getText());         // employee_id
        pstmt.setString(2, tfName.getText());           // name
        pstmt.setString(3, tfPosition.getText());       // position
        pstmt.setString(4, tfSalary.getText());                     // salary
        pstmt.setDouble(5, bonus);                      // bonus
        pstmt.setDouble(6, deductions);                 // deductions
        pstmt.setString(7, type);                       // type
        pstmt.setInt(8, hours);                         // hours
        pstmt.setDouble(9, total_salary);               // total_salary

        pstmt.executeUpdate();

        JOptionPane.showMessageDialog(null, "Payroll record saved successfully!");

