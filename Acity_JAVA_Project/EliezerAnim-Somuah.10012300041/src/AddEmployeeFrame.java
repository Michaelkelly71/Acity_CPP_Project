    private void tfSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchKeyReleased
        // TODO add your handling code here:
        
try {
    String sql = "SELECT * FROM employees WHERE id = ?";
    pstmt = conn.prepareStatement(sql);
    pstmt.setString(1, tfSearch.getText());
    rs = pstmt.executeQuery();

    if (rs.next()) {
        tfName.setText(rs.getString("name"));
        tfEmail.setText(rs.getString("email"));
        tfAddress.setText(rs.getString("address"));
        tfPhoneNumber.setText(rs.getString("phone_number"));
        tfDepartment.setText(rs.getString("department"));
        tfPosition.setText(rs.getString("position"));
        tfSalary.setText(rs.getString("salary"));

        String type = rs.getString("type");
        if (type.equalsIgnoreCase("hourly")) {
            rHourly.setSelected(true);
            rSalaried.setSelected(false);
        } else {
            rSalaried.setSelected(true);
            rHourly.setSelected(false);
        }
    } else {
        // Clear all fields
        tfName.setText("");
        tfEmail.setText("");
        tfAddress.setText("");
        tfPhoneNumber.setText("");
        tfDepartment.setText("");
        tfPosition.setText("");
        tfSalary.setText("");
        rSalaried.setSelected(false);
        rHourly.setSelected(false);

        // Show message
        JOptionPane.showMessageDialog(null, "No employee found with ID: " + tfSearch.getText());
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
} finally {
    try {
        if (rs != null) rs.close();
        if (pstmt != null) pstmt.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error closing resources: " + e.getMessage());
    }
}

    }//GEN-LAST:event_tfSearchKeyReleased

