    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        int x = JOptionPane.showConfirmDialog(null, "Are you sure you want to update employee salary","Employee Salary updated successfully",JOptionPane.YES_NO_OPTION);
        if (x==0){
        try {
            // Validate inputs
            String salaryText = tfSalary.getText().trim();
            String percText = tfPercentage.getText().trim();
            String amountText = tfAmount.getText().trim();
        
            if (salaryText.isEmpty() || (!percText.isEmpty() && !percText.matches("\\d+")) || (!amountText.isEmpty() && !amountText.matches("\\d+"))) {
                JOptionPane.showMessageDialog(null, "Please enter valid numbers only.");
                return;
            }
        
            int salary = Integer.parseInt(salaryText);
        
            // Apply percentage increase if provided
            if (!percText.isEmpty()) {
                int getPercentage = Integer.parseInt(percText);
                int calcPercentage = salary + (salary * getPercentage / 100);
                salary = calcPercentage;
            }
        
            // Apply flat amount increase if provided
            if (!amountText.isEmpty()) {
                int getAmount = Integer.parseInt(amountText);
                salary += getAmount;
            }
        
            tfSalary.setText(String.valueOf(salary)); // update salary field
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter only valid numbers for salary, percentage, or amount.");
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            return;
        }
        
        try {
            String value1 = tfName.getText();
            String value2 = tfSalary.getText();
            String id = tfSearch.getText();
        
            if (value1.isEmpty() || value2.isEmpty() || id.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please ensure Name, Salary, and ID are all filled.");
                return;
            }
        
            String sql = "UPDATE employees SET name = ?, salary = ? WHERE id = ?";
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, value1);
            pstmt.setString(2, value2);
            pstmt.setString(3, id);
        
            pstmt.executeUpdate();
        
            JOptionPane.showMessageDialog(null, "Salary Updated Successfully");
        
            // Clear fields
            tfName.setText("");
            tfSalary.setText("");
            tfSearch.setText("");
            tfPosition.setText("");
            tfPercentage.setText("");
            tfAmount.setText("");
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (pstmt != null) pstmt.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error closing resources: " + e.getMessage());
            }
        }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

