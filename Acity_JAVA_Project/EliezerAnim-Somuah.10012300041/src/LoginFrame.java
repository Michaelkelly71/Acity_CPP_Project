    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
      
        String sql = "select id,name,password from users where name =?";

        try {
            // Check if username or password is empty
            if (tfUserName.getText().isEmpty() || pfPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Username or Password cannot be empty");
                return;  // Exit the method if empty fields are found
            }
            
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, tfUserName.getText());
            
            rs = pstmt.executeQuery();
            
            // Check if user exists
            if (rs.next()) {
                // Username found, now check password
                String storedPassword = rs.getString("password");
                String loggedinuser = rs.getString("name");
                Emp.empname = loggedinuser;
        
                if (storedPassword.equals(pfPassword.getText())) {
                    // Correct password
                    MainFrame nf = new MainFrame();
                    nf.setVisible(true);
                    this.dispose();
                } else {
                    // Incorrect password
                    JOptionPane.showMessageDialog(null, "Incorrect Password");
                }
            } else {
                // Username does not exist
                JOptionPane.showMessageDialog(null, "Incorrect Username");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                if (rs != null) rs.close();
                if (pstmt != null) pstmt.close();
            } catch (Exception e) {
                // Ignore errors during close
            }
        }
            
    }//GEN-LAST:event_btnLoginActionPerformed

