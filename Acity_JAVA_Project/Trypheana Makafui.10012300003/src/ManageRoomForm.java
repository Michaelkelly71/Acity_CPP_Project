    private void RemoveRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveRoomActionPerformed
        // TODO add your handling code here:
        
               try {
                
                String removeQuery = "DELETE FROM rooms WHERE roomnumber=?";
                ps = conn.prepareStatement(removeQuery);
                ps.setString(1,jTextFieldRoomNum.getText());
                
                int rowAffected = ps.executeUpdate();
                    
                if(rowAffected >0 ){
                    JOptionPane.showMessageDialog(rootPane, "Room Deleted Successfully", "Remove Room", 2 );
                    jTextFieldRoomNum.setText("");
                    jTextFieldRoomType.setText("");

                } else{
                    JOptionPane.showMessageDialog(null, "No room found");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ManageClientsForm.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_RemoveRoomActionPerformed

