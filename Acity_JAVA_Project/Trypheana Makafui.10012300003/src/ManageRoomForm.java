    private void AddReservationsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddReservationsActionPerformed
             try {
                // TODO add your handling code here:
                
                String sql = "INSERT INTO reservations(roomnumber,client_id,date_in,date_out) VALUES (?,?,?,?)";
                ps = conn.prepareStatement(sql);
                ps.setString(1, jTextFieldIRoomnum2.getText());
                ps.setString(2, jTextFieldIClientID.getText());
                ps.setString(3, jTextFieldDateIN.getText());
                ps.setString(4, jTextFieldIDateOUT.getText());
                
                ps.execute();
                JOptionPane.showMessageDialog(rootPane, "New Reservation Added Successfully", "Add Reservation", 2 );
                
                jTextFieldIRoomnum2.setText("");
                jTextFieldIClientID.setText("");
                jTextFieldDateIN.setText("");
                jTextFieldIDateOUT.setText("");
               
            } catch (SQLException ex) {
                Logger.getLogger(ManageRoomForm.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_AddReservationsActionPerformed

