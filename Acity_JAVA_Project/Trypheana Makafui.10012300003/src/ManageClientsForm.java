    private void jButtonREMOVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonREMOVEActionPerformed
            try {
                // TODO add your handling code here:
                
                String removeQuery = "DELETE FROM clients WHERE id=?";
                ps = conn.prepareStatement(removeQuery);
                ps.setString(1,jTextFieldID.getText());
                
                int rowAffected = ps.executeUpdate();
                    
                if(rowAffected >0 ){
                    JOptionPane.showMessageDialog(rootPane, "Client Deleted Successfully", "Remove Client", 2 );
                    jTextFieldID.setText("");
                    jTextFieldFNAME.setText("");
                    jTextFieldLNAME.setText("");
                    jTextFieldCONTACT.setText("");
                    jTextFieldEMAIL.setText("");
                } else{
                    JOptionPane.showMessageDialog(null, "No client found");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ManageClientsForm.class.getName()).log(Level.SEVERE, null, ex);
            }
                    
    }//GEN-LAST:event_jButtonREMOVEActionPerformed

    private void jButtonEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEDITActionPerformed
            // TODO add your handling code here:
            
            String val = jTextFieldID.getText();
            String val1 = jTextFieldFNAME.getText();
            String val2 = jTextFieldLNAME.getText();
            String val3 = jTextFieldCONTACT.getText();
            String val4 = jTextFieldEMAIL.getText();
            
            String sql = "UPDATE clients SET id=?, firstname=?, lastname=?, contact=?, email=? WHERE id=? ";
            
            try {
                ps = conn.prepareStatement(sql);
                ps.setString(1, val);
                ps.setString(2, val1);
                ps.setString(3, val2);
                ps.setString(4, val3);
                ps.setString(5, val4);
                
                int rowsUpdated = ps.executeUpdate();
                if(rowsUpdated > 0){
                    JOptionPane.showMessageDialog(null, "Client edited");
                } else{
                    JOptionPane.showMessageDialog(null, "No client found");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ManageClientsForm.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButtonEDITActionPerformed

