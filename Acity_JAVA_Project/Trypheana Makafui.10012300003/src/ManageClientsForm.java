    private void jButtonADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonADDActionPerformed
        
            try {
                String addQuery = "INSERT INTO clients(id,firstname, lastname, contact, email) VALUES(?,?,?,?,?)";
                
                ps = conn.prepareStatement(addQuery);
                ps.setString(1, jTextFieldID.getText());
                ps.setString(2, jTextFieldFNAME.getText());
                ps.setString(3, jTextFieldLNAME.getText());
                ps.setString(4, jTextFieldCONTACT.getText());
                ps.setString(5, jTextFieldEMAIL.getText());
                
                ps.execute();
                JOptionPane.showMessageDialog(rootPane, "New Client Added Successfully", "Add Client", 2 );
                
                jTextFieldID.setText("");
                jTextFieldFNAME.setText("");
                jTextFieldLNAME.setText("");
                jTextFieldCONTACT.setText("");
                jTextFieldEMAIL.setText("");
                
            } catch (SQLException ex) {
                Logger.getLogger(ManageClientsForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_jButtonADDActionPerformed

