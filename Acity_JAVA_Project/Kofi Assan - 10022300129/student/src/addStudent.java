    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    try {
    stmt = conn.createStatement();

    String stdName = name.getText();
    String stdParent = parentName.getText();
    String Level = (String) stdLevel.getSelectedItem();
    String stdPhone = stdPhone_no.getText();
    String City = stdCity.getText();
    String Course = (String) stdCourse.getSelectedItem();

    String checkSql = "SELECT COUNT(*) AS enrolledCount FROM STUDENT WHERE stdCourse = '" + Course + "'";
    ResultSet rs = stmt.executeQuery(checkSql);
    int enrolledCount = 0;
    if (rs.next()) {
        enrolledCount = rs.getInt("enrolledCount");
    }
  
    String capacitySql = "SELECT max_capacity FROM COURSES WHERE stdCourse = '" + Course + "'";
    rs = stmt.executeQuery(capacitySql);
    int maxCapacity = 0;
    if (rs.next()) {
        maxCapacity = rs.getInt("max_capacity");
    }

    if (enrolledCount < maxCapacity) {
        int coursePosition = enrolledCount + 1;
        String sql = "INSERT INTO STUDENT(stdName, stdParent, stdLevel, stdPhone, stdCity, stdCourse, coursePosition) " +
        "VALUES('" + stdName + "', '" + stdParent + "', '" + Level + "', '" + stdPhone + "', '" + City + "', '" + Course + "', " + coursePosition + ")";
        
        stmt.executeUpdate(sql);
        int studentNumber = enrolledCount + 1;
        String message = """
                         Student registered successfully!
                         You are student No.""" + studentNumber + " out of " + maxCapacity + " in " + Course;
        JOptionPane.showMessageDialog(null, message);
        
    } else {
        JOptionPane.showMessageDialog(null, "Registration failed: Course is full.");
    }

    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

