    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int stdId = Integer.parseInt(id.getText());
            String stdName = name.getText();
            String stdParent = parentName.getText();
            String Level = (String) level.getSelectedItem();
            String stdPhone = stdPhone_no.getText();
            String City = stdCity.getText();
            String Course = (String) stdCourse.getSelectedItem();

            String sql = "UPDATE STUDENT SET stdName = '"+stdName+"', stdParent = '"+stdParent+"', stdLevel = '"+Level+"', stdPhone = '"+stdPhone+"', stdCity = '"+City+"', stdCourse = '"+Course+"'WHERE id = '"+stdId+"'";
            
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Record Updated");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

