    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int stdId = Integer.parseInt(id.getText());
            String sql = "SELECT * FROM STUDENT WHERE id = '"+stdId+"' ";
            rs = stmt.executeQuery(sql);
            
            if(rs.next()){
                name.setText(rs.getString("stdName"));
                parentName.setText(rs.getString("stdParent"));
                level.setSelectedItem(rs.getString("stdLevel"));
                stdPhone_no.setText(rs.getString("stdPhone"));
                stdCity.setText(rs.getString("stdCity"));
                stdCourse.setSelectedItem(rs.getString("stdCourse"));
            }else{
                JOptionPane.showMessageDialog(null, "Record Not Found");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        
        }
    }//GEN-LAST:event_jButton3ActionPerformed

