            String adminName = name.getText();
            String adminEmail = mail.getText();
            String adminPass = password.getText();

            String sql = "UPDATE ADMIN SET mail = '"+adminEmail+"', password = '"+adminPass+"', name = '"+adminName+"' WHERE id = '+adminId+'";
